package com.dataset;

/**
 * 表示一篇文章
 * @author Reacher
 *
 */
public class Item {
	private String content;
	private String label;
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
}
